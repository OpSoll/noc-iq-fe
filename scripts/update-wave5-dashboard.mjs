#!/usr/bin/env node
/**
 * update-wave5-dashboard.mjs
 * Fetches Wave 5 issue counts from GitHub and writes docs/WAVE5_DASHBOARD.md.
 * Requires GITHUB_TOKEN env var for issue API access.
 */
const REPO = "OpSoll/noc-iq-fe";
const WAVE5_LABEL = "Stellar Wave";
const token = process.env.GITHUB_TOKEN;

async function fetchIssues(state) {
  const headers = token ? { Authorization: `Bearer ${token}` } : {};
  const url = `https://api.github.com/repos/${REPO}/issues?state=${state}&labels=${encodeURIComponent(WAVE5_LABEL)}&per_page=100`;
  const res = await fetch(url, { headers });
  if (!res.ok) return [];
  return res.json();
}

const [open, closed] = await Promise.all([fetchIssues("open"), fetchIssues("closed")]);
const total = open.length + closed.length;
const pct = total ? Math.round((closed.length / total) * 100) : 0;
const now = new Date().toISOString().slice(0, 10);

const md = `# Wave 5 Closure Dashboard

> Last updated: ${now}

## Issue Completion

| Metric | Value |
|--------|-------|
| Total Wave 5 issues | ${total} |
| Closed | ${closed.length} |
| Open | ${open.length} |
| Completion | ${pct}% |

## Open Issues

${open.length ? open.map((i) => `- [ ] #${i.number} ${i.title}`).join("\n") : "_All issues closed! 🎉_"}

## Closed Issues

${closed.length ? closed.map((i) => `- [x] #${i.number} ${i.title}`).join("\n") : "_None closed yet._"}

---
_Auto-generated by \`scripts/update-wave5-dashboard.mjs\`. Do not edit manually._
`;

import { writeFileSync } from "fs";
writeFileSync("docs/WAVE5_DASHBOARD.md", md);
console.log(`✅ WAVE5_DASHBOARD.md updated (${closed.length}/${total} closed, ${pct}%)`);
